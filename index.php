    $headers[] = "MIME-Version: 1.0";
    $headers[] = "Content-Type: text/plain; charset=UTF-8";
    $headers[] = "From: {$siteName} <{$fromEmail}>";
    $headers[] = "Reply-To: {$form["name"]} <{$form["email"]}>";

    $sent = @mail($emailTo, "[Website] New Inquiry", $body, implode("\r\n", $headers));

    if ($sent) {
      $success = true;
