      "Name: {$form["name"]}\n" .
      "Email: {$form["email"]}\n" .
      "Phone: " . ($form["phone"] ?: "-") . "\n" .
      "Budget Range: " . ($form["budget"] ?: "-") . "\n\n" .
      "Message:\n{$form["message"]}\n\n" .
      "----\nIP: {$ip}\nUser-Agent: {$ua}\n";

